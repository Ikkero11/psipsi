async def start_patient(update, u, pid):
    p = PATIENTS[pid]
    vn = next_visit(u, pid)
    u.update({'current_patient': pid, 'current_visit': vn,
               'current_stage': 0, 'trust': 5, 'state': 'in_session'})
    u['energy'] -= 1
    caption = f"{p['name']}, {p['age']} лет | {p['job']}\nВизит {vn} из {p['visits_total']}"
    await send_photo(update, p['image'], caption)
