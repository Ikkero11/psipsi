def kb(*rows):
    buttons = []
    for row in rows:
        if row:
            buttons.append([InlineKeyboardButton(t, callback_data=d) for t, d in row])
    return InlineKeyboardMarkup(buttons) if buttons else None

def stats_bar(u: dict) -> str:
    e = min(u["energy"], 5)
    s = min(u["stress"], 5)
    return (
        f"Энергия:  {'🧠' * e}{'⬜' * (5 - e)}\n"
        f"Стресс:   {'😰' * s}{'⬜' * (5 - s)}\n"
        f"⭐ Репутация: {u['reputation']}"
    )

async def send(update: Update, text: str, markup=None):
    target = update.callback_query.message if update.callback_query else update.message
    await target.reply_text(text, reply_markup=markup, parse_mode="Markdown")

def next_visit_num(u: dict, pid: str) -> int:
    return u["patient_visits"].get(pid, 0) + 1

def patient_finished(u: dict, pid: str) -> bool:
    return u["patient_visits"].get(pid, 0) >= PATIENTS[pid]["visits_total"]

