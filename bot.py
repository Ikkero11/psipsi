def kb(*rows):
    buttons = [[InlineKeyboardButton(t, callback_data=d) for t, d in row]
               for row in rows if row]
    return InlineKeyboardMarkup(buttons) if buttons else None

def stats_line(u):
    e = min(u['energy'], 5)
    r = min(u['reputation'], 5)
    s = min(u['stress'] // 20, 5)
    return (
        f"🧠 Энергия: {'█' * e}{'░' * (5-e)} {u['energy']}/5\n"
        f"⭐ Репутация: {'★' * r}{'☆' * (5-r)} {u['reputation']}/5\n"
        f"😰 Стресс: {'▓' * s}{'░' * (5-s)} {u['stress']}/100"
    )

async def send(update, text, markup=None):
    target = update.callback_query.message if update.callback_query else update.message
    await target.reply_text(text, reply_markup=markup, parse_mode='Markdown')

async def send_photo(update, image_key, caption=''):
    target = update.callback_query.message if update.callback_query else update.message
    fn = IMAGES.get(image_key, '')
    if fn and os.path.exists(fn):
        try:
            with open(fn, 'rb') as f:
                await target.reply_photo(photo=f, caption=caption, parse_mode='Markdown')
            return
        except TelegramError as e:
            logger.warning(f'Фото {fn}: {e}')
    if caption:
        await send(update, f'🖼 _{caption}_')

def next_visit(u, pid):
    return u['patient_visits'].get(pid, 0) + 1

def patient_done(u, pid):
    return u['patient_visits'].get(pid, 0) >= PATIENTS[pid]['visits_total']

def can_take_more(u):
    return len(u['patients_today']) < u['day_limit'] and u['energy'] > 0

def apply_outcome(u, rep_d, stress_d):
    u['reputation'] = max(0, min(5, u['reputation'] + rep_d))
    u['stress'] = max(0, min(100, u['stress'] + stress_d))
    return u['stress'] >= 100

