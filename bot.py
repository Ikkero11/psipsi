async def cmd_start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    u = get_user(update.effective_user.id)
    await send(update,
        "🏥 *КАБИНЕТ*\n\n"
        "Вы — молодой специалист в психологической клинике.\n"
        "Перед вами — люди, которым нужна помощь.\n\n"
        "Читайте внимательно. Слушайте. Принимайте решения.\n\n"
        "_Иногда правильного ответа нет.\n"
        "Но ошибки имеют последствия._\n\n"
        "Удачи, доктор.",
        kb([("▶️ Начать работу", "menu")])
    )


async def callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    q = update.callback_query
    await q.answer()
    u = get_user(q.from_user.id)
    data = q.data

    if data == "menu":
        await show_main_menu(update, u)

    elif data == "start_session":
        await show_patient_select(update, u)

    elif data.startswith("patient_"):
        pid = data[len("patient_"):]
        await start_patient(update, u, pid)

    elif data.startswith("choice_"):
        cid = data[len("choice_"):]
        if cid.startswith("diag_"):
            await finalize_patient(update, u, u["current_patient"], cid)
        elif cid.startswith("end_"):
            await end_visit(update, u, u["current_patient"], u["current_visit"], cid)
        else:
            await handle_choice(update, u, cid)

    elif data == "new_day":
        await do_new_day(update, u)

    elif data == "check_outcomes":
        await show_outcomes(update, u)

    elif data.startswith("mat_quiz_"):
        mat_id = data[len("mat_quiz_"):]
        await show_material_quiz(update, u, mat_id)

    elif data.startswith("mat_ans_"):
        parts   = data.split("_")
        ans_idx = int(parts[-1])
        mat_id  = "_".join(parts[2:-1])
        await handle_material_answer(update, u, mat_id, ans_idx)

    elif data == "show_stats":
        s     = u["stats"]
        total = s["correct"] + s["partial"] + s["wrong"]
        visits_done = sum(u["patient_visits"].values())
        await send(update,
            f"📊 *Ваша статистика*\n\n"
            f"✅ Верных диагнозов: {s['correct']}\n"
            f"⚠️ Частично верных: {s['partial']}\n"
            f"❌ Ошибок: {s['wrong']}\n"
            f"📋 Всего диагнозов: {total}\n"
            f"🗂 Всего визитов: {visits_done}\n\n"
            f"⭐ Репутация: {u['reputation']}\n"
            f"😰 Стресс: {u['stress']}/10\n"
            f"🧠 Энергия: {u['energy']}/5\n"
            f"📅 Текущий день: {u['day']}",
            kb([("🔙 Назад", "menu")])
        )

    elif data == "about":
        await send(update,
            "ℹ️ *О игре*\n\n"
            "«Кабинет» — нарративный симулятор психолога.\n\n"
            "Каждый пациент приходит на *несколько визитов* — курс. "
            "История раскрывается постепенно. "
            "Диагноз ставится только на последнем визите.\n\n"
            "📅 *В день:* 1–2 пациента\n"
            "🌅 *Завершить день:* появится кнопка после приёма\n"
            "📚 *Обучение:* после каждого нового дня\n"
            "📩 *Отчёты:* результаты ваших решений\n\n"
            "_Каждое решение имеет последствия._",
            kb([("🔙 Назад", "menu")])
        )

