async def callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    q = update.callback_query
    await q.answer()
    uid  = q.from_user.id
    u    = get_user(uid)
    data = q.data

    if data == 'menu':
        await show_menu(update, u)
    elif data == 'start_session':
        day = u['day']
        # Секретный после первого пациента (день 5, 6, 8)
        if (not u['secret_disabled'] and day in SECRET_SCHEDULE
                and SECRET_SCHEDULE[day] in ('dialogue', 'interrupt', 'confront')
                and not u['secret_done_today'] and len(u['patients_today']) == 1):
            await show_secret(update, u)
        else:
            await show_patient_select(update, u)
    elif data.startswith('patient_'):
        await start_patient(update, u, data[len('patient_'):])
    elif data.startswith('choice_'):
        cid = data[len('choice_'):]
        if cid.startswith('diag_'):
            await finalize_patient(update, u, u['current_patient'], cid)
        elif cid.startswith('end_'):
            await end_visit(update, u, u['current_patient'], u['current_visit'], cid)
        else:
            await handle_choice(update, u, cid)
    elif data.startswith('sec_'):
        await handle_secret(update, u, data)
    elif data == 'new_day':
        await do_new_day(update, u)
    elif data == 'check_outcomes':
        await show_outcomes(update, u)
    elif data.startswith('mat_quiz_'):
        await show_mat_quiz(update, u, data[len('mat_quiz_'):])
    elif data.startswith('mat_ans_'):
        parts = data.split('_')
        ans_idx = int(parts[-1])
        mat_id = '_'.join(parts[2:-1])
        await handle_mat_ans(update, u, mat_id, ans_idx)
    elif data == 'show_stats':
        s = u['stats']
        total = s['correct'] + s['partial'] + s['wrong']
        vis = sum(u['patient_visits'].values())
        await send(update,
            f"📊 *Статистика — Алексей Дуликов*\n\n"
            f"✅ Верных диагнозов: {s['correct']}\n"
            f"⚠️ Частично верных: {s['partial']}\n"
            f"❌ Ошибок: {s['wrong']}\n"
            f"📋 Финальных кейсов: {total}\n"
            f"🗂 Всего визитов: {vis}\n\n"
            f"{stats_line(u)}\n📅 День: {u['day']}",
            kb([('🔙 Назад', 'menu')])
        )
    elif data == 'about':
        await send(update,
            'ℹ️ *О игре*\n\n'
            '«Кабинет» — нарративный симулятор психолога.\n\n'
            'Вы — молодой специалист в клинике «Отражение».\n'
            '5 пациентов, каждый приходит на *3 визита*.\n'
            'Диагноз только на третьем.\n\n'
            '📅 *В день:* 1–2 пациента\n'
            '🌅 *Новый день:* обучающий блок + тест\n'
            '📩 *Отчёты:* результаты ваших решений\n'
            '🕵️ *Кое-что ещё:* иногда в клинике происходит странное…\n\n'
            '_Каждое решение имеет последствия._',
            kb([('🔙 Назад', 'menu')])
        )


