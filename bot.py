users: dict = {}

def make_user():
    return {
        'day': 1, 'energy': 5, 'reputation': 3, 'stress': 0,
        'patient_visits': {}, 'patients_today': [],
        'secret_disabled': False, 'secret_done_today': False,
        'current_patient': None, 'current_visit': 1, 'current_stage': 0,
        'trust': 5, 'state': 'menu',
        'material_index': 0,
        'stats': {'correct': 0, 'partial': 0, 'wrong': 0},
        'pending_outcomes': [],
        'day_limit': 2,
    }

def get_user(uid):
    if uid not in users:
        users[uid] = make_user()
    return users[uid]

def reset_user(uid):
    u = make_user()
    u['secret_disabled'] = True
    users[uid] = u
    return u

