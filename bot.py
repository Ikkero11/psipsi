async def show_main_menu(update: Update, u: dict):
    u["state"] = "menu"
    taken = len(u["patients_today"])
    limit = u["day_patient_limit"]
    can_take = taken < limit and u["energy"] > 0

    if can_take:
        day_note = f"_Сегодня принято: {taken}/{limit}_"
        action_btn = [("🚪 Принять пациента", "start_session")]
    elif u["energy"] <= 0:
        day_note = "_😔 Энергия исчерпана_"
        action_btn = [("🌅 Завершить день", "new_day")]
    else:
        day_note = f"_✅ На сегодня все пациенты приняты ({taken}/{limit})_"
        action_btn = [("🌅 Завершить день", "new_day")]

    reports_btn = [("📩 Отчёты", "check_outcomes")] if u["pending_outcomes"] else []
    pending_note = f"\n📩 Ждут отчёты: {len(u['pending_outcomes'])}" if u["pending_outcomes"] else ""

    text = (
        f"🏥 *КАБИНЕТ — День {u['day']}*\n\n"
        f"{stats_bar(u)}\n\n"
        f"{day_note}{pending_note}"
    )
    markup = kb(action_btn, reports_btn, [("📊 Статистика", "show_stats"), ("ℹ️ О игре", "about")])
    await send(update, text, markup)


async def show_patient_select(update: Update, u: dict):
    available = [
        p for pid, p in PATIENTS.items()
        if pid not in u["patients_today"] and not patient_finished(u, pid)
    ]
    if not available:
        await send(update,
            "✅ *Все доступные пациенты на сегодня приняты.*",
            kb([("🌅 Завершить день", "new_day")])
        )
        return

    rows = []
    for p in available:
        v = next_visit_num(u, p["id"])
        total = p["visits_total"]
        label = f"👤 {p['name']}, {p['age']} лет  •  Визит {v}/{total}"
        rows.append([(label, f"patient_{p['id']}")])
    rows.append([("🔙 Назад", "menu")])

    await send(update,
        "👥 *Выберите пациента:*\n\n_Читайте внимательно — каждое слово важно._",
        InlineKeyboardMarkup([[InlineKeyboardButton(t, callback_data=d)] for row in rows for t, d in row])
    )


async def start_patient(update: Update, u: dict, patient_id: str):
    p = PATIENTS[patient_id]
    visit_num = next_visit_num(u, patient_id)

    u["current_patient"] = patient_id
    u["current_visit"]   = visit_num
    u["current_stage"]   = 0
    u["trust"]           = 5
    u["last_choice"]     = None
    u["state"]           = "in_session"
    u["energy"]         -= 1

    intro = p["first_intro"] if visit_num == 1 else p["return_intros"].get(
        visit_num, f"📋 *{p['name']} | Визит {visit_num}*\n\nПациент снова у вас."
    )
    await send(update, intro)
    await asyncio.sleep(0)
    await show_stage(update, u, p, visit_num, 0)


async def show_stage(update: Update, u: dict, p: dict, visit_num: int, stage_idx: int):
    u["current_stage"] = stage_idx
    stage = p["visit_stages"][visit_num][stage_idx]
    narration = stage.get("narration", "")
    rows = [[(c["text"], f"choice_{c['id']}")] for c in stage["choices"]]
    markup = InlineKeyboardMarkup([[InlineKeyboardButton(t, callback_data=d)] for row in rows for t, d in row])
    await send(update, narration, markup)


async def handle_choice(update: Update, u: dict, choice_id: str):
    pid       = u["current_patient"]
    visit_num = u["current_visit"]
    stage_idx = u["current_stage"]
    p         = PATIENTS[pid]
    stages    = p["visit_stages"][visit_num]

    # Доверие
    if choice_id in ("doubt", "pressure"):
        u["trust"] = max(0, u["trust"] - 2)
    elif choice_id == "support":
        u["trust"] = min(10, u["trust"] + 1)
    u["last_choice"] = choice_id

    next_idx = stage_idx + 1

    if next_idx < len(stages):
        next_stage = stages[next_idx]
        # Показать ответ пациента
        if "responses" in next_stage and choice_id in next_stage["responses"]:
            await send(update, next_stage["responses"][choice_id])
            await asyncio.sleep(0)
        await show_stage(update, u, p, visit_num, next_idx)
    else:
        # Конец визита: последняя стадия
        last_stage = stages[stage_idx]
        if last_stage["id"].endswith("_final"):
            await finalize_patient(update, u, pid, choice_id)
        else:
            await end_visit(update, u, pid, visit_num, choice_id)


async def end_visit(update: Update, u: dict, patient_id: str, visit_num: int, end_choice: str):
    p = PATIENTS[patient_id]
    u["patient_visits"][patient_id] = visit_num
    u["patients_today"].append(patient_id)
    u["state"] = "post_session"

    # Реакция пациента на конец сессии
    end_resp = p.get("visit_end_responses", {}).get(end_choice, "")
    if end_resp:
        await send(update, end_resp)
        await asyncio.sleep(0)

    next_v = visit_num + 1
    total  = p["visits_total"]

    # Показываем кнопку: либо ещё пациент, либо завершить день
    taken = len(u["patients_today"])
    limit = u["day_patient_limit"]
    if taken < limit and u["energy"] > 0:
        extra_btn = [("🚪 Принять следующего", "start_session")]
    else:
        extra_btn = [("🌅 Завершить день", "new_day")]

    await send(update,
        f"📁 *Сессия завершена*\n\n"
        f"_{p['name']} — Визит {visit_num}/{total}_\n\n"
        f"Следующая встреча: Визит {next_v}.",
        kb(extra_btn, [("🏥 В меню", "menu")])
    )


async def finalize_patient(update: Update, u: dict, patient_id: str, diag_id: str):
    p = PATIENTS[patient_id]
    icon, _ = p["outcomes"][diag_id]

    u["patient_visits"][patient_id] = p["visits_total"]
    u["patients_today"].append(patient_id)
    u["state"] = "post_session"

    if icon == "✅":
        u["stats"]["correct"] += 1
        u["reputation"] += 3
        u["stress"] = max(0, u["stress"] - 1)
    elif icon == "⚠️":
        u["stats"]["partial"] += 1
        u["reputation"] += 1
        u["stress"] += 1
    else:
        u["stats"]["wrong"] += 1
        u["stress"] += 2

    u["pending_outcomes"].append((patient_id, diag_id))

    taken = len(u["patients_today"])
    limit = u["day_patient_limit"]
    if taken < limit and u["energy"] > 0:
        extra_btn = [("🚪 Принять следующего", "start_session")]
    else:
        extra_btn = [("🌅 Завершить день", "new_day")]

    await send(update,
        f"📁 *Курс завершён*\n\n"
        f"_{p['name']} — все {p['visits_total']} визита пройдены._\n\n"
        f"Ваше решение зафиксировано. Результат — в отчёте 📩",
        kb(extra_btn, [("🏥 В меню", "menu")])
    )


async def do_new_day(update: Update, u: dict):
    u["day"] += 1
    u["patients_today"] = []
    u["energy"] = 5
    u["day_patient_limit"] = 2
    if u["stress"] > 0:
        u["stress"] -= 1

    mat = MATERIALS[u["material_index"] % len(MATERIALS)]
    u["material_index"] += 1
    u["state"] = f"material_{mat['id']}"

    await send(update,
        f"🌅 *День {u['day']}*\n\n"
        f"Вы отдохнули. Энергия восстановлена.\n"
        f"Перед приёмом — немного теории."
    )
    await asyncio.sleep(0)
    await send(update,
        f"📚 *Обучающий блок*\n\n{mat['title']}\n\n{mat['text']}",
        kb([("✅ Понял — проверить себя", f"mat_quiz_{mat['id']}")])
    )


async def show_material_quiz(update: Update, u: dict, mat_id: str):
    mat = next(m for m in MATERIALS if m["id"] == mat_id)
    rows = [[(opt["text"], f"mat_ans_{mat_id}_{i}")] for i, opt in enumerate(mat["options"])]
    rows.append([("⏭ Пропустить", "menu")])
    await send(update,
        f"❓ *Вопрос:*\n\n{mat['question']}",
        InlineKeyboardMarkup([[InlineKeyboardButton(t, callback_data=d)] for row in rows for t, d in row])
    )


async def handle_material_answer(update: Update, u: dict, mat_id: str, ans_idx: int):
    mat = next(m for m in MATERIALS if m["id"] == mat_id)
    opt = mat["options"][ans_idx]
    if opt["correct"]:
        resp = "✅ *Верно.* Хороший специалист."
    else:
        correct = next(o["text"] for o in mat["options"] if o["correct"])
        resp = f"❌ *Не совсем.* Правильный ответ: _{correct}_"
    await send(update, resp)
    await asyncio.sleep(0)
    await show_main_menu(update, u)


async def show_outcomes(update: Update, u: dict):
    if not u["pending_outcomes"]:
        await send(update, "📭 Нет новых отчётов.", kb([("🔙 Назад", "menu")]))
        return

    text = "📩 *Отчёты по вашим пациентам:*\n\n"
    for patient_id, diag_id in u["pending_outcomes"]:
        p = PATIENTS[patient_id]
        icon, outcome = p["outcomes"][diag_id]
        text += f"{icon} *{p['name']}*\n_{outcome}_\n\n"

    u["pending_outcomes"] = []
    await send(update, text, kb([("🔙 В меню", "menu")]))

